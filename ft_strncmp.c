#include "libft.h"

int	ft_strncmp(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	if ((s1 == NULL && s2 == NULL) || n == 0)
		return (0);
	if (s1 == NULL || s2 == NULL)
		return (1);
	i = -1;
	n--;
	while (++i < n && s1[i] && s2[i])
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
